from spack.package import *


class RPolished(RPackage):
	"""Authentication and Hosting for 'shiny' Apps

	Authentication, user administration, hosting, and additional infrastructure for 'shiny' apps. See
  <https://polished.tech> for additional documentation and examples.
	"""
	
